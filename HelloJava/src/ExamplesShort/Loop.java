package ExamplesShort;

public class Loop {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++){
            int two = 2;
            System.out.println(two + "*" + i +"=" + (two*i));
        }
        for (int i = 1; i < 10; i++){
            int three = 3;
            System.out.println(three + "*" + i + "=" + (three*i));
        }
        for (int i = 1; i < 5; i++){
            System.out.print(i);
        }
        for (int i = 5; i >= 1; i--){
            System.out.println(i);
        }


    }
}
